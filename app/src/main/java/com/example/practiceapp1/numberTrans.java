package com.example.practiceapp1;

public class numberTrans {
    private Integer num;
    private String word;

    public numberTrans(Integer num, String word)
    {
        this.num = num;
        this.word = word;
    }

    public Integer getNum() {
        return num;
    }

    public String getWord() {
        return word;
    }
}
